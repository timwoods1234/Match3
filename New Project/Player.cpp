#include "Player.h"

//-------------------------------------------------------------------------------
Player::Player()
:	m_nScore(0),
	m_nContinueCost(1)
{
}

//-------------------------------------------------------------------------------
Player::~Player()
{
}

//-------------------------------------------------------------------------------
void Player::Reset()
{
	m_nScore = 0;
}

//-------------------------------------------------------------------------------
void Player::AddScore(int points)
{
	m_nScore += points;
}

//-------------------------------------------------------------------------------
int Player::GetScore() const
{
	return m_nScore;
}

//-------------------------------------------------------------------------------
int Player::GetContinueCost() const
{
	return m_nContinueCost;
}

//-------------------------------------------------------------------------------
void Player::SetContinueCost(int cost)
{
	m_nContinueCost = cost;
}
